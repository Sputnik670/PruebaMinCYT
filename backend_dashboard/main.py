# --- HERRAMIENTAS ---
def limpiar_dinero(valor):
    if pd.isna(valor): return 0.0
    s = str(valor).replace("$", "").replace(" ", "").strip()
    if "," in s:
        s = s.replace(".", "").replace(",", ".")
    return float(s) if s else 0.0

def cargar_csv(url):
    try:
        # Si la URL es el placeholder, no hacemos nada
        if "TU_LINK" in url: return None
        
        response = requests.get(url)
        response.raise_for_status()
        
        # --- CORRECCIÓN DE CARACTERES ---
        # Forzamos la decodificación a UTF-8 al leer el contenido
        # response.content es bytes, io.BytesIO lo maneja mejor que StringIO para pandas con encoding
        df = pd.read_csv(io.BytesIO(response.content), encoding='utf-8')
        
        df = df.fillna("")
        return df
    except Exception as e:
        print(f"Error cargando CSV {url}: {e}")
        return None

